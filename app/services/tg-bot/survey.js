export function summarizeAnswers(answers = {}) {
  const segments = [];

  if (answers.goals) {
    segments.push(`–¶–µ–ª–∏: ${answers.goals}`);
  }

  if (answers.audience) {
    segments.push(`–ê—É–¥–∏—Ç–æ—Ä–∏—è: ${answers.audience}`);
  }

  if (answers.offer) {
    segments.push(`–û—Ñ—Ñ–µ—Ä: ${answers.offer}`);
  }

  if (answers.keyMessages) {
    segments.push(`–°–æ–æ–±—â–µ–Ω–∏—è: ${answers.keyMessages}`);
  }

  if (answers.callToAction) {
    segments.push(`CTA: ${answers.callToAction}`);
  }

  if (segments.length === 0) {
    return '–ë—Ä–∏—Ñ –ø–æ–∫–∞ –∑–∞–ø–æ–ª–Ω–µ–Ω —á–∞—Å—Ç–∏—á–Ω–æ ‚Äî –æ–±–Ω–æ–≤–∏—Ç–µ –¥–∞–Ω–Ω—ã–µ —á–µ—Ä–µ–∑ /setup.';
  }

  return segments.join('\n');
}

